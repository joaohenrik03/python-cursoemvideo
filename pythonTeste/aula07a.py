n1 = int(input('Um valor: '))
n2 = int(input('Outro valor: '))
soma = n1 + n2
multi = n1 * n2
divisao = n1 / n2
divint = n1 // n2
exponen = n1 ** n2
print('A soma é {}, a multiplicação é {} e a divisão é {} '.format(soma, multi, divisao), end='')
print('Divisão inteira {} e potência {}.'.format(divint, exponen))
